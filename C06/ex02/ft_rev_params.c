#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j;
	int	i;

	j = argc;
	while (j > 1 && *argv[j -1] != '\0')
	{
		i = 0;
		while (argv[j -1][i] != '\0')
		{
			write (1, &argv[j -1][i], 1);
			i++;
		}
		write (1, "\n", 1);
		j--;
	}
}
